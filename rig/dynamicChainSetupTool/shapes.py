import maya.cmds as cmds

def commands(controlCurve):
	##centers the pivot of the created curve
	##renames the curve's shape to match the transform name
	if controlCurve:
		controlCurveShape = cmds.listRelatives(controlCurve, shapes=True)[0]
		cmds.rename(controlCurveShape, "%sShape"%controlCurve)
		cmds.xform(controlCurve, cp=True)

